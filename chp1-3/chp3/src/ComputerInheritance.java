
class ComputerInheritance {

    // variables
    private double cpuSpeed;  // in GHz
    // constructor
    ComputerInheritance() {
        cpuSpeed = 0;
    }
    // methods
    void setCpuSpeed ( double _cpuSpeed ) {
        cpuSpeed = _cpuSpeed;
    }
    double getCpuSpeed () {
        return cpuSpeed;
    }



}
